#  Build classifier label
# Priority: use sensor-derived label if available, else derive from EEA thresholds
def eea_danger(row):
    checks = {
        "no2": EEA_THRESHOLDS.get("no2", 200),
        "co":  EEA_THRESHOLDS.get("co",   10),
        "so2": EEA_THRESHOLDS.get("so2",  125),
        "o3":  EEA_THRESHOLDS.get("o3",   120),
    }
    for col, thresh in checks.items():
        if col in row and pd.notna(row[col]) and row[col] > thresh:
            return 1
    return 0

