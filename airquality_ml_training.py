"""## 7. Train Prophet Models (Temperature & Humidity Forecast)
Prophet is designed for time series with seasonality and missing data.  
We train one model per target. Each produces a **7-day ahead forecast**.

"""

from prophet import Prophet
from sklearn.metrics import mean_absolute_error, mean_squared_error

def train_prophet(df, target_col, label):
    """Train a Prophet model and return (model, forecast_df, metrics)."""
    sub = df[["date", target_col]].dropna().rename(
        columns={"date": "ds", target_col: "y"}
    )

    if len(sub) < 14:
        print(f"  ⚠️  Not enough data for {label} ({len(sub)} rows) — skipping")
        return None, None, None

    # Train / test split  last 14 days as hold-out
    split = sub["ds"].max() - pd.Timedelta(days=14)
    train = sub[sub["ds"] <= split]
    test  = sub[sub["ds"] >  split]

    model = Prophet(
        yearly_seasonality  = True,
        weekly_seasonality  = True,
        daily_seasonality   = False,
        seasonality_mode    = "additive",
        changepoint_prior_scale = 0.05,
    )
    model.fit(train)

    # Predict on test + 7 days ahead
    future = model.make_future_dataframe(periods=14 + 7)
    forecast = model.predict(future)

    # Metrics on hold-out
    merged = test.merge(forecast[["ds","yhat"]], on="ds")
    if len(merged) > 0:
        mae  = mean_absolute_error(merged["y"], merged["yhat"])
        rmse = mean_squared_error(merged["y"], merged["yhat"]) ** 0.5
        print(f"  {label:30s}  MAE={mae:.3f}  RMSE={rmse:.3f}  (hold-out {len(merged)} days)")
    else:
        print(f"  {label:30s}  trained (no hold-out overlap)")

    return model, forecast, sub

print("Training Prophet models...")
